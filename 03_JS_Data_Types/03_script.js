// number
let jsVersion = 6;
console.log(`Value : ${jsVersion} Type : ${typeof jsVersion}`);

// string
let empName = ' Williams';
console.log(`Value : ${empName} Type : ${typeof empName}`);

// boolean
let isJSEasy = true;
console.log(`Value : ${isJSEasy} Type : ${typeof isJSEasy}`);

// Re-assignments
let abc;
abc = 100;
abc = 'test';
abc = true;
abc = null;
console.log(`Value : ${abc} Type : ${typeof abc}`);


